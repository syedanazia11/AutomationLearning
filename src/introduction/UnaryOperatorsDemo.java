package introduction;

public class UnaryOperatorsDemo {

	public static void main(String[] args) {
	
		// ++, --, !
		
		int i = 10;
		int j = i--; //Post-decrement
		
		System.out.println("i = "+i);
		System.out.println("j = "+j);
		
		
		int x = 10;
		int y = --x;
		
		System.out.println("x "+x);
		System.out.println("y "+y);

	}

}
